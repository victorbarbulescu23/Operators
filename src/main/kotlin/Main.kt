fun main() {
    var sum: Double
    var diff: Double
    var product: Double
    var quotient: Double
    var mod: Double

    var x = 5.0
    var y = 2.0

    sum = x + y
    diff = x - y
    product = x * y
    quotient = (x/y)
    mod = x%y

    println("$x + $y = $sum")
    println("$x - $y = $diff")
    println("$x * $y = $product")
    println("$x / $y = $quotient")
    println("$x % $y = $mod")

}